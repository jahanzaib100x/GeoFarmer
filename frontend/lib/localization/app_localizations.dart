  String translate(String key) {
    // If specific language is active, pull from it; otherwise fallback to isUrdu toggling
    String lang = activeLanguage;
    if (lang == 'en' && isUrdu) {
      lang = 'ur';
    }
    
    // Safety fallback cascading dictionary lookup
    return _localizedValues[lang]?[key] ?? 
           _localizedValues['ur']?[key] ?? 
           _localizedValues['en']?[key] ?? 
           key;
  }
